int		ft_sqrt(int nb)
{
	int i;
	int c;

	i = 1;
	c = 0;
	if (nb <= 0 || nb > 2147395600)
	{
		return (0);
	}
	while (i <= nb / i)
	{
		c = c + i;
		if (c * c > nb)
		{
			return (0);
		}
		else if (c * c == nb)
		{
			return (c);
		}
	}
	return (c);
}
